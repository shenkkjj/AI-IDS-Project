#!/usr/bin/env node
// M3-01 返工验证脚本:证明 deriveBriefing 在 24h / 168h 窗口下,
// buckets.length 分别为 24 与 7(不允许 168 个桶)。
//
// 用法:
//   node docs/runs/2026-06-16-m3-verify-briefing-buckets.mjs
//
// 实现要点:
//  - Node 24 的 --experimental-strip-types 在 ESM 模式下要求 import 路径
//    带扩展名,生产 .ts 之间用无扩展名相对路径(Node 解析失败)。
//    为避免修改生产代码,本脚本把生产 .ts 源码读出来,按依赖顺序拼成单个
//    临时 .ts,并把跨文件 import 行抹掉,然后 spawn node --experimental-strip-types
//    一次性跑。临时文件写到 os.tmpdir(),跑完立即删除,不污染工作区。
//  - 这样保证断言的是"生产代码"而不是"复刻的逻辑"。
//  - process.exitCode 表示断言结果,非 0 即失败。

import { readFileSync, writeFileSync, unlinkSync, existsSync } from "node:fs";
import { tmpdir } from "node:os";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { spawnSync } from "node:child_process";

const HERE = path.dirname(fileURLToPath(import.meta.url));
const PROJECT_ROOT = path.resolve(HERE, "..", "..");
const UTILS_DIR = path.join(PROJECT_ROOT, "web-next", "utils");
const PROD_BRIEFING = path.join(UTILS_DIR, "alertBriefingUtils.ts");
const PROD_ALERT_UTILS = path.join(UTILS_DIR, "alertUtils.ts");

for (const p of [PROD_BRIEFING, PROD_ALERT_UTILS]) {
  if (!existsSync(p)) {
    console.error("[FAIL] 未找到生产代码文件:", p);
    process.exit(2);
  }
}

const alertUtilsSrc = readFileSync(PROD_ALERT_UTILS, "utf8");
const briefingSrc = readFileSync(PROD_BRIEFING, "utf8");

// 抹掉所有 import 行:
//  - type-only import 会被 strip-types 擦除,但 @/ alias 仍可能让 Node 解析时尝试查找
//  - runtime import 必须去掉,否则 Node 找不到模块
// 这里只保留函数/常量等普通声明,把所有 import 行整行删除。
function stripImportLines(src) {
  return src
    .split(/\r?\n/)
    .filter((line) => !/^\s*import\b/.test(line))
    .join("\n");
}

const TMP_FILE = path.join(tmpdir(), "verify-briefing-buckets.tmp.ts");

// 拼接顺序: alertUtils(去掉 import) -> alertBriefingUtils(去掉 import) -> 测试代码
const wrapper =
  "// auto-generated by docs/runs/2026-06-16-m3-verify-briefing-buckets.mjs\n" +
  "// --- inlined alertUtils.ts ---\n" +
  stripImportLines(alertUtilsSrc) +
  "\n// --- inlined alertBriefingUtils.ts ---\n" +
  stripImportLines(briefingSrc) +
  "\n// --- verify ---\n" +
  "const now = Math.floor(Date.now() / 1000);\n" +
  "const mockAlerts = [\n" +
  '  { id: "A1", alertId: "A1", source: "1.1.1.1", target: "2.2.2.2",\n' +
  '    payload: "id=1 OR 1=1", risk: "critical", blocked: true,  timestamp: now - 60 },\n' +
  '  { id: "A2", alertId: "A2", source: "3.3.3.3", target: "4.4.4.4",\n' +
  '    payload: "<script>alert(1)</script>", risk: "high", blocked: false, timestamp: now - 7200 },\n' +
  "];\n" +
  "const b24  = deriveBriefing(mockAlerts, 24);\n" +
  "const b168 = deriveBriefing(mockAlerts, 168);\n" +
  "const results = [\n" +
  '  { name: "24h  -> buckets.length === 24",  ok: b24.buckets.length  === 24,  got: b24.buckets.length  },\n' +
  '  { name: "168h -> buckets.length === 7",   ok: b168.buckets.length === 7,   got: b168.buckets.length },\n' +
  '  { name: "24h  -> windowHours === 24",     ok: b24.windowHours     === 24,  got: b24.windowHours     },\n' +
  '  { name: "168h -> windowHours === 168",    ok: b168.windowHours    === 168, got: b168.windowHours    },\n' +
  "];\n" +
  "let allOk = true;\n" +
  "for (const r of results) {\n" +
  '  console.log((r.ok ? "  OK  " : " FAIL ") + r.name + "  (got: " + r.got + ")");\n' +
  "  if (!r.ok) allOk = false;\n" +
  "}\n" +
  'console.log("[bucket count] 24h=" + b24.buckets.length + "  168h=" + b168.buckets.length);\n' +
  'console.log("[sample bucket labels] 24h[0]=" + b24.buckets[0]?.time + "  168h[0]=" + b168.buckets[0]?.time);\n' +
  "process.exitCode = allOk ? 0 : 1;\n";

try {
  writeFileSync(TMP_FILE, wrapper, "utf8");
  const proc = spawnSync(
    "node",
    ["--experimental-strip-types", "--no-warnings", TMP_FILE],
    { encoding: "utf8", stdio: "inherit" }
  );
  if (proc.error) throw proc.error;
  if (proc.status !== 0) {
    console.error("[FAIL] node 子进程退出码 =", proc.status);
    process.exitCode = proc.status || 1;
  } else {
    console.log("[OK] deriveBriefing 桶数验证通过");
  }
} finally {
  try { unlinkSync(TMP_FILE); } catch {}
}
